static MENU_UPDATE_FUNC(adtg_update)
{
    MENU_SET_ICON(MNI_BOOL(adtg_enabled), 0);
    if (adtg_enabled)
        MENU_SET_WARNING(MENU_WARN_INFO, "ADTG hooks enabled, press PLAY to disable.");
}

static MENU_SELECT_FUNC(adtg_main)
{
    if (delta > 0)
    {
        if (!adtg_enabled)
            adtg_toggle(priv, delta);
        menu_open_submenu();
    }
    else
    {
        if (adtg_enabled)
            adtg_toggle(priv, delta);
    }
}

